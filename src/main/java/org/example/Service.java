package org.example;

public class Service {
    private String name;

    public Service(String name) {
        this.name = name;
    }

    public void action() {
        System.out.println("Action");
    }

    public void think() {
        System.out.println("Think");
    }

    public void sleep() {
        System.out.println("Sleep");
    }

    public void run() {
        System.out.println("Run");
    }

    public void eat() {
        System.out.println("Eat");
    }

    public void love() {
        System.out.println("Love");
    }
}
